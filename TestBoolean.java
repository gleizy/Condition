
public class TestBoolean {

	public static void main(String[] args) {
		
		boolean test = false;
		
		test = true;
		
		//Not(!)
		test = !test; //test = false
		test = !test; //test = true
		
		//And(&&)
		//true && true = true
		//true && false = false
		//false && true = false
		//false && false = false
		
		//Or(||)
		//true || true = true
		//true || false = true
		//false || true = true
		//false || false = false
		
	}

}
